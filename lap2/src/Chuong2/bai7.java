public class bai7 {
    public static void main(String[] args) {
        // Số lượng số nguyên dương cần in
        int count = 20;

        // In ra 20 số nguyên dương đầu tiên
        System.out.println("20 so nguyen duong dau tien:");
        for (int i = 1; i <= count; ++i) {
            System.out.println(i);
        }
    }
}
